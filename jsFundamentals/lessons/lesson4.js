//Relational or Comparison Operators

// >  Greater Than
// <  Less Than
// >= Greater Than Or Equal To
// <= Less Than Or Equal To
// == Equal To

var result = 5 > 3
console.log(result);

//Equality Operators

var x = 1
console.log(x == '1');   //Loose Comparison
console.log(x === '1');  //Strict Comparison
console.log(x === 1);